// Custom minimal non-blocking toast elements
function showCustomToast(title, body, type = 'success') {
    const container = document.getElementById('toast-container');
    const toast = document.createElement('div');
    
    let colorClasses = 'border-neutral-200 dark:border-neutral-800 text-neutral-800 dark:text-neutral-200';
    let iconClass = 'fa-solid fa-circle-check text-accent-cyan';
    if (type === 'error') {
        colorClasses = 'border-red-200 dark:border-red-950 text-red-500';
        iconClass = 'fa-solid fa-triangle-exclamation text-red-400';
    }

    toast.className = `modern-glass p-4 rounded-2xl border ${colorClasses} shadow-lg flex gap-3 pointer-events-auto transition-all duration-500 transform translate-y-2 opacity-0`;
    toast.innerHTML = `
        <div class="flex-shrink-0 text-sm mt-0.5"><i class="${iconClass}"></i></div>
        <div class="space-y-0.5 text-xs flex-grow">
            <h5 class="font-bold text-inherit">${title}</h5>
            <p class="text-neutral-500 dark:text-neutral-400 leading-relaxed">${body}</p>
        </div>
        <button onclick="this.parentElement.remove()" class="text-neutral-400 hover:text-neutral-600 dark:hover:text-neutral-200"><i class="fa-solid fa-xmark text-xs"></i></button>
    `;

    container.appendChild(toast);
    setTimeout(() => {
        toast.classList.remove('translate-y-2', 'opacity-0');
    }, 100);

    setTimeout(() => {
        toast.classList.add('translate-y-2', 'opacity-0');
        setTimeout(() => toast.remove(), 500);
    }, 6000);
}

// --- FORMSPREE SUBMISSION LOGIC ---
function handleFormSubmit(event) {
    event.preventDefault();
    const form = event.target;
    const submitBtn = form.querySelector('button[type="submit"]');
    const submitText = document.getElementById('form-submit-text');
    const originalText = submitText.innerHTML;

    // Change button state to sending
    submitBtn.disabled = true;
    if (currentLang === 'fa') {
        submitText.innerText = "در حال ارسال...";
    } else {
        submitText.innerText = "Transmitting...";
    }

    const data = new FormData(form);

    fetch('https://formspree.io/f/mjgdypyq', {
        method: 'POST',
        body: data,
        headers: {
            'Accept': 'application/json'
        }
    })
    .then(response => {
        submitBtn.disabled = false;
        submitText.innerHTML = originalText;
        
        if (response.ok) {
            const nameVal = document.getElementById('form-name').value;
            if (currentLang === 'fa') {
                showCustomToast(
                    "پیام مخابره شد",
                    `ممنون از شما ${nameVal}! پیام شما با موفقیت ارسال شد. به زودی با شما تماس خواهم گرفت.`,
                    'success'
                );
            } else {
                showCustomToast(
                    "Message Transmitted",
                    `Thank you, ${nameVal}! Your request was successfully transmitted. I will get back to you shortly.`,
                    'success'
                );
            }
            form.reset();
        } else {
            response.json().then(data => {
                const errMsg = data.errors ? data.errors.map(err => err.message).join(', ') : '';
                if (currentLang === 'fa') {
                    showCustomToast("خطا در ارسال", `مشکلی رخ داد: ${errMsg || 'لطفا مجدد تلاش کنید'}`, 'error');
                } else {
                    showCustomToast("Submission Error", `Issue: ${errMsg || 'Please try again'}`, 'error');
                }
            });
        }
    })
    .catch(error => {
        submitBtn.disabled = false;
        submitText.innerHTML = originalText;
        if (currentLang === 'fa') {
            showCustomToast("خطای شبکه", "اتصال برقرار نشد. لطفا ارتباط اینترنت خود را چک کنید.", 'error');
        } else {
            showCustomToast("Network Error", "Unable to reach server. Please check your connection.", 'error');
        }
    });
}

