const canvas = document.getElementById('sandbox-canvas');
const ctx = canvas.getContext('2d');
const fpsCounter = document.getElementById('fps-counter');

let particles = [];
let mouse = { x: null, y: null, radius: 120 };
let gridCols = 15;
let gridRows = 15;
let springStrength = 0.08;
let friction = 0.9;

// Physics Sliders Connections
const sliderGravity = document.getElementById('slider-gravity');
const sliderElastic = document.getElementById('slider-elastic');
const gravityVal = document.getElementById('gravity-val');
const elasticVal = document.getElementById('elastic-val');
const resetBtn = document.getElementById('reset-physics');

sliderGravity.addEventListener('input', (e) => {
    mouse.radius = parseInt(e.target.value);
    gravityVal.innerText = e.target.value;
});

sliderElastic.addEventListener('input', (e) => {
    springStrength = parseFloat(e.target.value) / 100;
    elasticVal.innerText = springStrength.toFixed(2);
});

resetBtn.addEventListener('click', () => {
    initMesh();
    // Reset Sliders
    sliderGravity.value = 120;
    sliderElastic.value = 8;
    mouse.radius = 120;
    springStrength = 0.08;
    gravityVal.innerText = "120";
    elasticVal.innerText = "0.08";
});

class PhysicsParticle {
    constructor(x, y) {
        this.x = x;
        this.y = y;
        this.baseX = x;
        this.baseY = y;
        this.vx = 0;
        this.vy = 0;
        this.radius = 2.5;
    }

    update() {
        let dxBase = this.baseX - this.x;
        let dyBase = this.baseY - this.y;
        
        this.vx += dxBase * springStrength;
        this.vy += dyBase * springStrength;

        if (mouse.x !== null && mouse.y !== null) {
            let dxMouse = this.x - mouse.x;
            let dyMouse = this.y - mouse.y;
            let distance = Math.sqrt(dxMouse * dxMouse + dyMouse * dyMouse);

            if (distance < mouse.radius) {
                let force = (mouse.radius - distance) / mouse.radius;
                let angle = Math.atan2(dyMouse, dxMouse);
                
                this.vx += Math.cos(angle) * force * 14;
                this.vy += Math.sin(angle) * force * 14;
            }
        }

        this.vx *= friction;
        this.vy *= friction;

        this.x += this.vx;
        this.y += this.vy;
    }

    draw() {
        ctx.beginPath();
        ctx.arc(this.x, this.y, this.radius, 0, Math.PI * 2);
        
        let dx = this.x - this.baseX;
        let dy = this.y - this.baseY;
        let displacement = Math.sqrt(dx * dx + dy * dy);
        let hue = displacement * 12 + 190;
        
        ctx.fillStyle = `hsla(${hue}, 90%, 65%, ${Math.min(0.8, 0.4 + displacement / 15)})`;
        ctx.shadowBlur = displacement > 3 ? displacement : 0;
        ctx.shadowColor = `hsla(${hue}, 95%, 65%, 0.8)`;
        ctx.fill();
        ctx.shadowBlur = 0;
    }
}

function initMesh() {
    particles = [];
    const rect = canvas.getBoundingClientRect();
    canvas.width = rect.width * devicePixelRatio;
    canvas.height = rect.height * devicePixelRatio;
    ctx.scale(devicePixelRatio, devicePixelRatio);

    const padding = 20;
    const w = rect.width - padding * 2;
    const h = rect.height - padding * 2;
    const colSpacing = w / (gridCols - 1);
    const rowSpacing = h / (gridRows - 1);

    for (let r = 0; r < gridRows; r++) {
        for (let c = 0; c < gridCols; c++) {
            let px = padding + c * colSpacing;
            let py = padding + r * rowSpacing;
            particles.push(new PhysicsParticle(px, py));
        }
    }
}

canvas.addEventListener('click', (e) => {
    const rect = canvas.getBoundingClientRect();
    const clickX = e.clientX - rect.left;
    const clickY = e.clientY - rect.top;

    particles.forEach(p => {
        let dx = p.x - clickX;
        let dy = p.y - clickY;
        let distance = Math.sqrt(dx * dx + dy * dy);
        if (distance < 180) {
            let angle = Math.atan2(dy, dx);
            p.vx += Math.cos(angle) * 45;
            p.vy += Math.sin(angle) * 45;
        }
    });
});

canvas.addEventListener('mousemove', (e) => {
    const rect = canvas.getBoundingClientRect();
    mouse.x = e.clientX - rect.left;
    mouse.y = e.clientY - rect.top;
});

canvas.addEventListener('mouseleave', () => {
    mouse.x = null;
    mouse.y = null;
});

canvas.addEventListener('touchmove', (e) => {
    if (e.touches.length > 0) {
        const rect = canvas.getBoundingClientRect();
        mouse.x = e.touches[0].clientX - rect.left;
        mouse.y = e.touches[0].clientY - rect.top;
    }
}, { passive: true });

canvas.addEventListener('touchend', () => {
    mouse.x = null;
    mouse.y = null;
});

let lastTime = 0;
let frameCount = 0;
let fps = 60;

function drawMesh() {
    ctx.clearRect(0, 0, canvas.width, canvas.height);

    const now = performance.now();
    frameCount++;
    if (now > lastTime + 1000) {
        fps = Math.round((frameCount * 1000) / (now - lastTime));
        fpsCounter.innerText = `${fps} FPS • ACTIVE`;
        frameCount = 0;
        lastTime = now;
    }

    ctx.lineWidth = 0.55;
    for (let r = 0; r < gridRows; r++) {
        for (let c = 0; c < gridCols; c++) {
            const idx = r * gridCols + c;
            const p1 = particles[idx];

            if (c < gridCols - 1) {
                const p2 = particles[idx + 1];
                drawLink(p1, p2);
            }
            if (r < gridRows - 1) {
                const p2 = particles[idx + gridCols];
                drawLink(p1, p2);
            }
        }
    }

    particles.forEach(p => {
        p.update();
        p.draw();
    });

    requestAnimationFrame(drawMesh);
}

function drawLink(p1, p2) {
    let dx = p1.x - p2.x;
    let dy = p1.y - p2.y;
    let dist = Math.sqrt(dx * dx + dy * dy);

    let dxOrig = Math.abs(p1.baseX - p2.baseX);
    let dyOrig = Math.abs(p1.baseY - p2.baseY);
    let baseDist = Math.sqrt(dxOrig * dxOrig + dyOrig * dyOrig);
    let strain = Math.max(0, dist - baseDist);

    let isDark = document.documentElement.classList.contains('dark');
    let baseAlpha = isDark ? 0.15 : 0.25;

    ctx.beginPath();
    ctx.moveTo(p1.x, p1.y);
    ctx.lineTo(p2.x, p2.y);
    
    if (strain > 2) {
        ctx.strokeStyle = `hsla(${190 + strain * 5}, 90%, 65%, ${Math.min(0.8, baseAlpha + strain / 10)})`;
        ctx.lineWidth = Math.min(1.5, 0.55 + strain / 8);
    } else {
        ctx.strokeStyle = isDark ? `rgba(6, 182, 212, ${baseAlpha})` : `rgba(139, 92, 246, ${baseAlpha})`;
        ctx.lineWidth = 0.55;
    }
    ctx.stroke();
}

